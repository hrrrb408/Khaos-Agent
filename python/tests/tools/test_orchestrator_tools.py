"""Tests for Phase 8.3 orchestrator tools.

The orchestrator tool handlers delegate to module-level globals (``_spawner`` /
``_runner``) injected by :func:`init_orchestrator`. Each test stubs those
globals via monkeypatch and asserts the handlers return the documented
``{"ok": ..., ...}`` contract — no real AgentLoop / DB is exercised here.
"""
