async def test_collect_results_success(reset_globals):
    tasks = [
        _StubTask("t1", "goal 1", "completed", result="r1"),
        _StubTask("t2", "goal 2", "failed", result=None, error="boom"),
    ]
    spawner = _make_spawner(wait_all_return=tasks)
    init_orchestrator(spawner, MagicMock())

