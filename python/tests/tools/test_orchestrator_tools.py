# ──────────────────────────── registry wiring ──────────────────────────


def test_runtime_registry_wires_orchestrator_handlers():
    """create_runtime_registry must attach the 4 orchestrator handlers."""
    registry = create_runtime_registry()
    for name in ("spawn_subagent", "collect_results", "execute_plan", "subagent_status"):
        tool = registry.get(name)
        assert tool.handler is not None, f"{name} handler not wired"
        assert callable(tool.handler)


def test_orchestrator_tools_available_in_office_and_coding():
    registry = create_runtime_registry()
    office_names = {t.name for t in registry.list_by_mode("office")}
    coding_names = {t.name for t in registry.list_by_mode("coding")}
    for name in ("spawn_subagent", "collect_results", "execute_plan", "subagent_status"):
        assert name in office_names, f"{name} not available in office mode"
        assert name in coding_names, f"{name} not available in coding mode"


def test_orchestrator_permission_levels():
    registry = create_runtime_registry()
    assert registry.get("spawn_subagent").permission_level == "write"
    assert registry.get("collect_results").permission_level == "read"
    assert registry.get("execute_plan").permission_level == "write"
    assert registry.get("subagent_status").permission_level == "read"
